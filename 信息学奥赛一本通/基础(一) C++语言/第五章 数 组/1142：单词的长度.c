#include <stdio.h>
#include <string.h>

int main()
{
    char str[1001];  // 定义一个字符数组，用于存储输入的字符串
    int cnt[300] = { 0 }, i = 0, j = 0, wcnt = 0;  // 定义一些变量，包括一个用于存储每个单词长度的数组，一个用于遍历字符串的索引，一个用于记录当前单词的长度，以及一个用于记录当前处理的是第几个单词
    fgets(str, 1001, stdin);  // 从标准输入读取一个字符串
    while (str[i] != 0)  // 遍历字符串
    {
        if(str[i] == ' ')  // 如果当前字符是空格，说明一个单词已经结束
        {
            cnt[j] = wcnt;  // 将当前单词的长度存入数组
            if(j == 0 && cnt[j] != 0)  // 如果是第一个单词，且长度不为0，直接输出长度
            {
                printf("%d", cnt[j]);
            }
            else if (cnt[j] != 0)  // 如果不是第一个单词，且长度不为0，输出逗号和长度
            {
                printf(",%d", cnt[j]);
            }
            j++;  // 处理下一个单词
            wcnt = 0;  // 重置单词长度为0
        }
        else  // 如果当前字符不是空格，说明单词还没有结束
        {
            wcnt++;  // 单词长度加1
        }
        i++;  // 处理下一个字符
    }
    // 处理最后一个单词的长度
    if (wcnt != 0)  // 如果最后一个单词的长度不为0，输出逗号和长度
    {
        printf(",%d", wcnt - 1);
    }
    return 0;
}